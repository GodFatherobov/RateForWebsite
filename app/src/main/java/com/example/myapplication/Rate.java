package com.example.myapplication;

public class Rate {
    private int id;
    private String url;
    private int likecount;
    private int dislikecount;
    private float rate;

    public int getId() {
        return id;
    }

    public String getUrl(){
        return url;
    }
    public int getLikecount(){
        return likecount;
    }
    public int getDislikecount(){
        return dislikecount;
    }
    public float getRate()
    {
        return rate;
    }
}
